/** 전체 교육과정 트리 (과목 순서: 국어·수학·영어·과학·사회) */
export const CURRICULUM: CurriculumCourse[] = [
  ...KOREAN,
  ...MATH,
  ...ENGLISH,
  ...SCIENCE,
  ...SOCIAL,
];

/** 과목별 과정 목록 (UI: 과목 선택 → 과정 그룹) */
export function coursesBySubject(subject: Subject): CurriculumCourse[] {
  return CURRICULUM.filter((c) => c.subject === subject);
}

/** 과정 id로 조회 */
export function getCourse(courseId: string): CurriculumCourse | undefined {
  return CURRICULUM.find((c) => c.id === courseId);
}

/** 단원 id로 (과정+단원) 조회 — 엔진/시험지에서 라벨·focus 사용 */
export function findUnit(
  unitId: string
): { course: CurriculumCourse; unit: CurriculumUnit } | undefined {
  for (const course of CURRICULUM) {
    const unit = course.units.find((u) => u.id === unitId);
    if (unit) return { course, unit };
  }
  return undefined;
}

/** "수학Ⅰ · 수열" 같은 사람이 읽는 단원 경로 라벨 */
export function unitPathLabel(unitId: string): string | undefined {
  const found = findUnit(unitId);
  return found ? `${found.course.name} · ${found.unit.name}` : undefined;
}
