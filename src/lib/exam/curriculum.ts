import type { Subject } from "./types";

/** 교육과정 한 단원 */
export interface CurriculumUnit {
  /** 안정적 식별자 (과정-단원). 예: "math1-sequence" */
  id: string;
  /** 단원명 (교육과정 공시 명칭) */
  name: string;
  /** 소단원·핵심 출제 키워드 — 강사 선택 보조 + 엔진 출제 범위 한정 */
  topics: string[];
  /** 이 단원에서 평가하는 사고/빈출 유형 — 엔진 프롬프트에 주입(출제 정합성의 핵심) */
  focus: string;
  /** 이 단원의 대표 배점(수능/내신 기준). 시험지 배점 표기·엔진 난이도 힌트로 사용 */
  points: number[];
}

/** 과목 아래 '과정'(공통/선택과목·영역) 단위 */
export interface CurriculumCourse {
  /** 안정적 식별자. 예: "math-1" */
  id: string;
  subject: Subject;
  /** 과정명. 예: "수학Ⅰ", "독서", "물리학Ⅰ" */
  name: string;
  /** 공통 응시 여부(선택과목과 구분) — UI 그룹 표기용 */
  common?: boolean;
  units: CurriculumUnit[];
}

